#include "ft_printf.h"

void	ft_putnbr(long n, int *len)
{
	char	c;

	if (n < 0)
	{
		ft_putchar('-', len);
		n = -n;
	}
	if (n > 9)
		ft_putnbr(n / 10, len);
	c = n % 10 + '0';
	ft_putchar(c, len);
}
